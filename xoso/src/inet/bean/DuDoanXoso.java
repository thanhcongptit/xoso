package inet.bean;

public class DuDoanXoso {
	private int id;
	private String dayso;
	private String openDate;
	private int xu;
	private int vitri;
	
	public int getVitri() {
		return vitri;
	}
	public void setVitri(int vitri) {
		this.vitri = vitri;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDayso() {
		return dayso;
	}
	public void setDayso(String dayso) {
		this.dayso = dayso;
	}
	public String getOpenDate() {
		return openDate;
	}
	public void setOpenDate(String openDate) {
		this.openDate = openDate;
	}
	public int getXu() {
		return xu;
	}
	public void setXu(int xu) {
		this.xu = xu;
	}
	
}
