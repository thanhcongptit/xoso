public class ThongKeNhanh {
    private String capSo;
    private String code;
    private String ganNhat;
    private int xuatHien;
    private int chuaVe;

    public String getCapSo() {
        return capSo;
    }

    public void setCapSo(String capSo) {
        this.capSo = capSo;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getGanNhat() {
        return ganNhat;
    }

    public void setGanNhat(String ganNhat) {
        this.ganNhat = ganNhat;
    }

    public int getXuatHien() {
        return xuatHien;
    }

    public void setXuatHien(int xuatHien) {
        this.xuatHien = xuatHien;
    }

    public int getChuaVe() {
        return chuaVe;
    }

    public void setChuaVe(int chuaVe) {
        this.chuaVe = chuaVe;
    }
}
